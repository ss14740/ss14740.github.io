var vid1 = document.getElementById("vid1");

// Add event listener for mouseenter event
vid1.addEventListener("mouseenter", function() {
    vid1.play();
});

// Add event listener for mouseleave event
vid1.addEventListener("mouseleave", function() {
    vid1.pause();
});

